    {
        $this->newLine();

        $this->line(<<<EOT
         <fg=blue>.______       ___       __       ________   __   __   __. 
                  |   _  \    /   \     |  |     |       /  |  | |  \ |  | 
                  |  |_)  |  /  ^  \    |  |     `---/  /   |  | |   \|  | 
                  |   ___/  /  /_\  \   |  |        /  /    |  | |  . `  | 
                  |  |     /  _____  \  |  `----.  /  /----.|  | |  |\   | 
                  | _|    /__/     \__\ |_______| /________||__| |__| \__| 
                                                         
                        .___  ___.   ______   .__   __.  __  .___________.  ______   .______      
                        |   \/   |  /  __  \  |  \ |  | |  | |           | /  __  \  |   _  \     
                        |  \  /  | |  |  |  | |   \|  | |  | `---|  |----`|  |  |  | |  |_)  |    
                        |  |\/|  | |  |  |  | |  . `  | |  |     |  |     |  |  |  | |      /     
                        |  |  |  | |  `--'  | |  |\   | |  |     |  |     |  `--'  | |  |\  \----.
                        |__|  |__|  \______/  |__| \__| |__|     |__|      \______/  | _| `._____|   </>
        EOT);

        $this->newLine(1);
    }
}
